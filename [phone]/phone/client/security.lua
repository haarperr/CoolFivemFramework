RegisterNUICallback('FingerprintScan', function(data, cb)
    local noGloves = true
    local sex = "male"
    if not exports.data:getCharVar("sex") then
        sex = "female"
    end
    for k, v in each(exports['fivem-appearance']:getPedComponents(PlayerPedId())) do
        if k == 4 then
            if Gloves[sex][tostring(v.drawable)] then
                noGloves = false
                break
            end
        end
    end
    cb(noGloves)
end)

